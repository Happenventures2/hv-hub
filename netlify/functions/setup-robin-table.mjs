export default async (req) => {
  try {
    const url = new URL(req.url);
    if (url.searchParams.get("confirm") !== "yes") {
      return json({
        message: "This endpoint creates the Robin Tasks table. Add ?confirm=yes to run it.",
        tableName: TABLE_NAME,
        baseId: BASE_ID,
      });
    }

    const apiKey = Netlify.env.get("AIRTABLE_API_KEY");
    if (!apiKey) return json({ error: "AIRTABLE_API_KEY not configured" }, 500);

    // 1. Check if table already exists
    const listRes = await fetch(`https://api.airtable.com/v0/meta/bases/${BASE_ID}/tables`, {
      headers: { Authorization: `Bearer ${apiKey}` },
    });
    if (!listRes.ok) {
      const t = await listRes.text();
      return json({ error: `Failed to list tables: ${listRes.status}`, detail: t.slice(0, 500) }, 502);
    }
    const listData = await listRes.json();
    const existing = (listData.tables || []).find(t => t.name === TABLE_NAME);

    let tableId;
    let createdNow = false;

    if (existing) {
      tableId = existing.id;
    } else {
      // 2. Create the table
      const createRes = await fetch(`https://api.airtable.com/v0/meta/bases/${BASE_ID}/tables`, {
        method: "POST",
        headers: { Authorization: `Bearer ${apiKey}`, "Content-Type": "application/json" },
        body: JSON.stringify(TABLE_SCHEMA),
      });
      if (!createRes.ok) {
        const t = await createRes.text();
        return json({ error: `Failed to create table: ${createRes.status}`, detail: t.slice(0, 800) }, 502);
      }
      const created = await createRes.json();
      tableId = created.id;
      createdNow = true;

      // 3. Seed with sample tasks so dashboard shows something on first load
      const seedRes = await fetch(`https://api.airtable.com/v0/${BASE_ID}/${tableId}`, {
        method: "POST",
        headers: { Authorization: `Bearer ${apiKey}`, "Content-Type": "application/json" },
        body: JSON.stringify({
          records: SEED_TASKS.map(fields => ({ fields })),
          typecast: true,
        }),
      });
      if (!seedRes.ok) {
        const t = await seedRes.text();
        return json({
          warning: "Table created but seeding failed",
          tableId,
          createdNow: true,
          seedError: t.slice(0, 500),
        });
      }
    }

    return json({
      success: true,
      tableId,
      createdNow,
      message: createdNow
        ? `Created Robin Tasks table + seeded with ${SEED_TASKS.length} sample tasks`
        : "Robin Tasks table already exists",
      airtableUrl: `https://airtable.com/${BASE_ID}/${tableId}`,
      nextStep: "Update /api/ai-tasks to read from this table, then delete this setup endpoint.",
    });
  } catch (err) {
    console.error("setup-robin-table error:", err);
    return json({ error: err.message || "Unknown" }, 500);
  }
};

function json(body, status = 200) {
  return new Response(JSON.stringify(body, null, 2), {
    status,
    headers: { "Content-Type": "application/json" },
  });
}

